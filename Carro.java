package orientada_obj;

public class Carro {

	String modelo;
	String marca;
	String tipo;
	String cor;
	int acl;
	String farol;
	String direita;
	String esquerda;
	
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
	this.cor = cor;
	}
	public int getAcl() {
		return acl;
	}
	public void setAcl(int acl) {
		this.acl = acl;
	}
	public String getFarol() {
		return farol;
	}
	public void setFarol(String farol) {
		this.farol = farol;
	}
	public String getDireita() {
		return direita;
	}
	public void setDireita(String direita) {
		this.direita = direita;
	}
	public String getEsquerda() {
		return esquerda;
	}
	public void setEsquerda(String esquerda) {
		this.esquerda = esquerda;
}

}
